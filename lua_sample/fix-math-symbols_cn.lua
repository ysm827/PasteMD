-- LaTeX 常见数学符号误写及国内规范修复过滤器 (Pandoc Lua Filter)
-- 用途：自动修复 Markdown/HTML 转换 DOCX 或 PDF 时，数学公式中的非标准写法。
-- 
-- 使用方法：
-- 在 Pandoc 命令中添加： --lua-filter=fix-math-symbols.lua
-- 或在 config.json 中配置： "pandoc_filters": ["path/to/fix-math-symbols_cn.lua"]
-- 或在 PasteMD设置 中直接添加该过滤器路径

local replacements = {
  ---------------------------------------------------------
  -- 1. 平行符号误写
  ---------------------------------------------------------
  {"/\\/",      "\\parallel"},      -- 修复 /\/ 
  {"\\/\\/",    "\\parallel"},      -- 修复 \/\/
  {"/\\!/",     "\\parallel"},      -- 修复 /\!/ (常见于有些为了让双斜杠靠近的奇怪写法)

  ---------------------------------------------------------
  -- 2. 关系运算符的“编程习惯”误写
  ---------------------------------------------------------
  {"!=",        "\\neq"},           -- 修复 != 变为不等于
  {"==",        "\\equiv"},         -- 修复 == 变为恒等号 (或视需要改成 "=")
  
  ---------------------------------------------------------
  -- 3. 国内教材规范符号纠正
  ---------------------------------------------------------
  -- 注：西方默认 \le 是水平等号 ≦，但国内标准要求使用倾斜等号 ⩽ (\leqslant)
  {"<=",        "\\leqslant"},      -- 修复 <= 变为国内标准的小于等于
  {">=",        "\\geqslant"},      -- 修复 >= 变为国内标准的大于等于
  
  -- 注：西方默认 \emptyset 是压扁的零，国内标准是正圆形加斜线 \varnothing
  {"\\emptyset", "\\varnothing"},   -- 修正空集符号为国内标准

  ---------------------------------------------------------
  -- 4. 纯文本箭头误写
  ---------------------------------------------------------
  -- （Lua 的正则中 '-' 是魔术字符，需要用 '%' 转义）
  {"<=>",       "\\Leftrightarrow"},-- 修复 <=> 变为等价于
  {"=>",        "\\Rightarrow"},    -- 修复 => 变为推出
  {"<%-",       "\\gets"},          -- 修复 <- 变为左箭头
  {"%->",       "\\to"},            -- 修复 -> 变为右箭头

  ---------------------------------------------------------
  -- 5. 角度、度数误写
  ---------------------------------------------------------
  {"°",         "^\\circ"},         -- 修复直接输入的纯文本度数符号 °
  {"%^{o}",     "^\\circ"},         -- 修复 ^{o} (使用英文字母 o 的误写)
  {"%^o",       "^\\circ"},         -- 修复 ^o

  ---------------------------------------------------------
  -- 6. 省略号与标点误写
  ---------------------------------------------------------
  {"%.%.%.",    "\\dots"},          -- 修复 ... 变为标准的 \dots （自动处理居中或靠底）
  {"，",        ", "}               -- 修复公式里的全角中文逗号，替换为半角逗号加空格
}

local function fix_math_symbols(content)
  -- 遍历所有规则进行全局替换
  for _, rule in ipairs(replacements) do
    local pattern = rule[1]
    local replacement = rule[2]
    -- gsub 会返回两个值，我们只需要第一个(替换后的文本)
    content = content:gsub(pattern, replacement)
  end
  return content
end

return {
  {
    Math = function(el)
      -- 将公式内的文本进行格式修复
      el.text = fix_math_symbols(el.text)
      return el
    end
  }
}